const contract = new web3.eth.Contract(CONTRACT_ABI, CONTRACT_ADDRESS);

// Get balances
async function getEthBalance() {
    const balanceWei = await web3.eth.getBalance(account.address);
    console.log("ETH Balance:", web3.utils.fromWei(balanceWei, "ether"));
}

async function getGoldBalance() {
    const balance = await contract.methods.balanceOf(account.address).call();
    console.log("GOLD Balance:", web3.utils.fromWei(balance, "ether"));
}

// Buy GOLD
async function buyGold(grams) {
    const pricePerGram = await contract.methods.pricePerGram().call();
    const totalCost = new BN(pricePerGram).mul(new BN(web3.utils.toWei(grams.toString(), "ether")));

    const tx = await contract.methods.buyGold(web3.utils.toWei(grams.toString(), "ether"))
        .send({ from: account.address, value: totalCost.toString(), gas: 300000 });

    console.log("Transaction Hash:", tx.transactionHash);
}

